package com.cytx.dto;

/**
 * content里面的内容：病人信息
 * 
 * @author xilehang
 * 
 */
public class ContentDataDto {

	private String type;
	private String age;
	private String sex;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

}
